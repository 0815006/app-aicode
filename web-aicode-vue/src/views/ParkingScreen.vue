            { x: 500, y: 280, dir: 'vh', count: 9, dept: 'е…¶д»–е…¬еҸё', prefix: 'B', startNo: 87 }
          ],
          description: 'дёҠеҢ—дёӢеҚ—гҖҒе·ҰиҘҝеҸідёңж ҮеҮҶж–№дҪҚеёғеұҖгҖӮиҜҘеҢәеҹҹж јеұҖзӣёеҜ№зӢ¬з«ӢпјҢеҸідҫ§дёӯеҒҸдёӢи®ҫжңүB2ең°еә“еҮәеҸЈеҸҠеҚҠеј§еҪўеҮәеә“иЎҢиҪҰеқЎйҒ“гҖӮеҢ—еўҷйқ иҫ№жҺ’30дёӘз«–еҗ‘й»„иүІиҪҰдҪҚпјҲB018-B047пјүпјҢиҘҝеўҷйқ иҫ№жҺ’6дёӘжЁӘеҗ‘й»„иүІиҪҰдҪҚпјҲB048-B053пјүпјҢдёӯйғЁеҚ•жҺ’жӮ¬з©әеІӣ3дёӘз«–еҗ‘й»„иүІиҪҰдҪҚпјҲB054-B056пјүгҖӮдёӯдёӢйғЁеҸҠжңҖеә•з«ҜжңүеӨ§йқўз§ҜзҒ°иүІеҚ дҪҚиҪҰдҪҚйҳөеҲ—гҖӮе…ЁйғЁ39дёӘжҲ‘еҸёиҪҰдҪҚеқҮеұһдәҺејҖеҸ‘дёҖйғЁгҖӮ',
