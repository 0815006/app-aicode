  computed: {
    currentFloorData() {
      return this.floors.find(f => f.id === this.currentFloor) || this.floors[0]
    },
    // SVGж•ҙдҪ“transformпјҡ5еҸ·жҘјB1йңҖиҰҒж—ӢиҪ¬90В°дҪҝеҢ—жңқдёҠ
    currentFloorTransform() {
      const floor = this.currentFloorData
      if (floor && floor.svgTransform) {
        return floor.svgTransform
      }
      const xOff = (floor && floor.xOffset) || 0
      const yOff = (floor && floor.yOffset) || 0
      return 'translate(' + xOff + ',' + yOff + ')'
    },
    // SVG viewBoxпјҡж—ӢиҪ¬еҗҺзҡ„жҘјеұӮз»ҹдёҖз”ЁdisplayWidth/displayHeightпјҢдёҺ4еҸ·жҘјиҪҰдҪҚзӯүеӨ§
    currentViewBox() {
      const floor = this.currentFloorData
      if (floor && floor.displayWidth) {
        return '0 0 ' + floor.displayWidth + ' ' + floor.displayHeight
      }
      return '0 0 ' + (floor.width || 1200) + ' ' + (floor.height || 560)
    },
    // SVG и§Ҷеӣҫе®Ҫпјҡдјҳе…ҲдҪҝз”ЁdisplayWidth
    currentSvgWidth() {
      const floor = this.currentFloorData
      if (floor && floor.displayWidth) {
        return floor.displayWidth
      }
      return floor.width || 1200
    },
    // SVG и§Ҷеӣҫй«ҳпјҡдјҳе…ҲдҪҝз”ЁdisplayHeight
    currentSvgHeight() {
      const floor = this.currentFloorData
      if (floor && floor.displayHeight) {
        return floor.displayHeight
      }
      return floor.height || 560
    },
    // жҳҜеҗҰдёәи¶…й•ҝжҘјеұӮпјҲйңҖиҰҒж»ҡеҠЁжҹҘзңӢпјү
    isTallFloor() {
      const floor = this.currentFloorData
      return !!(floor && floor.displayWidth)
    },
    // еўҷдҪ“еҲ—иЎЁпјҲжқҘиҮӘ floor.wallsпјҢеӣӣи§’еҸҠеІӣеҶ…й—ҙйҡ”еўҷдҪ“пјү
    currentWalls() {
      const floor = this.currentFloorData
      if (!floor || !floor.walls) return []
      const walls = floor.walls.map(w => ({ ...w }))
      const spaces = this.currentSpaces
      const pg = 8 // pillar gap
      walls.forEach(w => {
        if (!w.dynamic) return
        const rowSpaces = spaces.filter(s => s.y === w.y).sort((a, b) => a.x - b.x)
        if (!rowSpaces.length) return
        if (w.dynamic === 'fill') {
          // еҢ—еўҷпјҡеЎ«ж»ЎеӨ–йғЁиҪҰдҪҚе’ҢеҶ…йғЁиҪҰдҪҚд№Ӣй—ҙзҡ„з©әйҡҷ
          const extSp = rowSpaces.filter(s => s.dept === 'е…¶д»–е…¬еҸё')
          const intSp = rowSpaces.filter(s => s.dept !== 'е…¶д»–е…¬еҸё')
          if (extSp.length && intSp.length) {
            w.x = Math.max(...extSp.map(s => s.x + s.w))
            w.w = Math.min(...intSp.map(s => s.x)) - w.x
          }
        } else {
          // еІӣеҶ…еўҷпјҡе·Ұиҫ№з•Ң=еүҚдёҖдёӘиҪҰдҪҚеҸіиҫ№з•Ң+жҹұеӯҗй—ҙйҡҷ
          const idx = rowSpaces.findIndex(s => s.x > w.x)
          if (idx > 0) {
            const prev = rowSpaces[idx - 1]
            w.x = prev.x + prev.w + pg
          }
          if (w.w === 0) {
            // е®ҪеәҰеЎ«еҲ°дёӢдёҖдёӘиҪҰдҪҚе·Ұиҫ№з•Ң-жҹұеӯҗй—ҙйҡҷ
            const idx2 = rowSpaces.findIndex(s => s.x >= w.x)
            if (idx2 >= 0 && idx2 < rowSpaces.length) {
              w.w = rowSpaces[idx2].x - pg - w.x
            }
          }
        }
      })
      return walls
    },
    // еӯҗжҜҚиҪҰдҪҚз»„еҗҲзҹ©еҪўпјҡе°ҶжҜҸдёӘеӯҗжҜҚиҪҰдҪҚдёҺе…¶йҷ„еұһй“әдҪҚжЎҶеңЁдёҖиө·
    tandemGroupRects() {
      const spaces = this.currentSpaces
      if (!spaces || spaces.length === 0) return []
      const groups = []
      const usedй“әдҪҚKeys = new Set()
      spaces.forEach(space => {
        if (!space.tandem) return
        // еҜ»жүҫдёҺжң¬еӯҗжҜҚиҪҰдҪҚзҙ§йӮ»зҡ„й“әдҪҚпјҲеҗҢxе·®55y=vhпјҢеҗҢyе·®55x=hvпјү
        const й“әдҪҚ = spaces.find(s =>
          !s.tandem &&
          ((space.vertical && s.x === space.x && Math.abs(s.y - space.y) === 55) ||
           (!space.vertical && s.y === space.y && Math.abs(s.x - space.x) === 55))
        )
        if (!й“әдҪҚ) return
        const key = й“әдҪҚ.x + ',' + й“әдҪҚ.y
        if (usedй“әдҪҚKeys.has(key)) return
        usedй“әдҪҚKeys.add(key)
        const pad = 3
        const minX = Math.min(space.x, й“әдҪҚ.x) - pad
        const minY = Math.min(space.y, й“әдҪҚ.y) - pad
        const maxX = Math.max(space.x + space.w, й“әдҪҚ.x + й“әдҪҚ.w) + pad
        const maxY = Math.max(space.y + space.h, й“әдҪҚ.y + й“әдҪҚ.h) + pad
        groups.push({ x: minX, y: minY, w: maxX - minX, h: maxY - minY, dept: space.dept })
      })
      return groups
    },
    currentSpaces() {
      const floor = this.currentFloorData
      if (!floor) return []
      const spaces = []
      const hW = 38, hH = 24, hGap = 3
      const vW = 24, vH = 38, vGap = 3
      // vhпјҡзәөеҗ‘иҪҰдҪҚпјҲиҪҰеӨҙеҚ—еҢ—пјүпјҢжІҝж°ҙе№іж–№еҗ‘жҺ’еҲ— - з¬ҰеҗҲз”ЁжҲ·и§„ж ј25Г—55
      const vhW = 25, vhH = 55, vhGap = 5
      // hvпјҡжЁӘеҗ‘иҪҰдҪҚпјҲиҪҰеӨҙдёңиҘҝпјүпјҢжІҝеһӮзӣҙж–№еҗ‘жҺ’еҲ— - з¬ҰеҗҲз”ЁжҲ·и§„ж ј55Г—25
      const hvW = 55, hvH = 25, hvGap = 5

      floor.blocks.forEach(block => {
        for (let i = 0; i < block.count; i++) {
          let x, y, w, h, vertical
          if (block.dir === 'h') {
            // жЁӘеҗ‘иҪҰдҪҚпјҢж°ҙе№іжҺ’еҲ—
            w = hW; h = hH; vertical = false
            x = block.x + i * (hW + hGap)
            y = block.y
          } else if (block.dir === 'vh') {
            // зәөеҗ‘иҪҰдҪҚпјҲиҪҰеӨҙеҚ—еҢ—пјүпјҢж°ҙе№іжҺ’еҲ—
            w = vhW; h = vhH; vertical = true
            // 4еҸ·жҘјB1пјҡжҹұеӯҗвҶ’3иҪҰдҪҚвҶ’жҹұеӯҗвҶ’3иҪҰдҪҚпјҲжҹұеӯҗе…ҲдәҺз¬¬дёҖз»„пјҢи·ЁиЎҢеҜ№йҪҗпјү
            if (floor.id === '4F-B1' || floor.id === '5F-B1') {
              const closeGap = 3, pillarGap = 8
              const g = Math.floor(i / 3)
              x = block.x + pillarGap + i * vhW + g * pillarGap + (i - g) * closeGap
            } else {
              x = block.x + i * (vhW + vhGap)
            }
            y = block.y
          } else if (block.dir === 'hv') {
            // жЁӘеҗ‘иҪҰдҪҚпјҲиҪҰеӨҙдёңиҘҝпјүпјҢеһӮзӣҙжҺ’еҲ—
            w = hvW; h = hvH; vertical = false
            x = block.x
            if (floor.id === '4F-B1' || floor.id === '5F-B1') {
              const closeGap = 3, pillarGap = 8
              const g = Math.floor(i / 3)
              y = block.y + pillarGap + i * hvH + g * pillarGap + (i - g) * closeGap
            } else {
              y = block.y + i * (hvH + hvGap)
            }
          } else if (block.dir === 'v') {
            // зәөеҗ‘иҪҰдҪҚпјҲиҪҰеӨҙеҚ—еҢ—пјүпјҢеһӮзӣҙжҺ’еҲ—пјҲз”ЁдәҺ5еҸ·жҘјеІӣеҶ…еҲ—пјү
            // еҺӢзј©й«ҳеәҰд»ҘйҖӮй…ҚSVGеҸҜз”Ёз©әй—ҙпјҡ25Г—(30+3)=33px/иЎҢ
            const vStepH = 30, vStepGap = 3
            w = vhW; h = vStepH; vertical = true
            x = block.x
            y = block.y + i * (vStepH + vStepGap)
          } else {
            // зәөеҗ‘иҪҰдҪҚпјҢеһӮзӣҙжҺ’еҲ—пјҲе…је®№ж—§ж јејҸпјү
            w = vW; h = vH; vertical = true
            x = block.x
            y = block.y + i * (vH + vGap)
          }
          const step = block.numberStep || 1
          const no = block.startNo + i * step
          const label = block.prefix === 'дёҙ'
            ? 'дёҙ' + no
            : block.prefix + String(no).padStart(3, '0')
          spaces.push({ x, y, w, h, dept: block.dept, label, vertical, tandem: !!block.tandem, disabled: !!block.disabled })
        }
      })
      return spaces
    }
  },
